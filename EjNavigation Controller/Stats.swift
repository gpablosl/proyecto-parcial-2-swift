
class Stats{
    var tier : String
    var salud : String
    var dmg : String
    var armadura : String
    var escudos : String
    
    
    init(tier : String, salud : String, dmg : String, armadura : String, escudos : String){
        self.tier = tier
        self.salud = salud
        self.dmg = dmg
        self.armadura = armadura
        self.escudos = escudos
    }
}
